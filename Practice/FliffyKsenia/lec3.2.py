# запрашиваем количество километров
x = int(input("Расстояние которое проехал автомбиль, (км): "))
# запрашиваем время которое ехал автомобиль.
y = int(input("Время которое проехал автомобиль, (ч): "))
# вычисляем сколько автомобиль проехал в среднем за час.
result = x / y
# Выводим результат на экран
print(f"Result: {result}")